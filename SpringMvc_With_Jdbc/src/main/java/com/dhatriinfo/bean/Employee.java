package com.dhatriinfo.bean;

public class Employee {
private String employeeId;
private String employeeName;
private float employeeSalary;
private float employeeExperiance;
private String employeeLocation;

public String getEmployeeId() {
	return employeeId;
}
public void setEmployeeId(String employeeId) {
	this.employeeId = employeeId;
}
public String getEmployeeName() {
	return employeeName;
}
public void setEmployeeName(String employeeName) {
	this.employeeName = employeeName;
}
public float getEmployeeSalary() {
	return employeeSalary;
}
public void setEmployeeSalary(float employeeSalary) {
	this.employeeSalary = employeeSalary;
}
public float getEmployeeExperiance() {
	return employeeExperiance;
}
public void setEmployeeExperiance(float employeeExperiance) {
	this.employeeExperiance = employeeExperiance;
}
public String getEmployeeLocation() {
	return employeeLocation;
}
public void setEmployeeLocation(String employeeLocation) {
	this.employeeLocation = employeeLocation;
}

}
